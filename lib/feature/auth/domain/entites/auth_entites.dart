class AuthEntity {
  final String message;
  final String token;
  final String name;
  final String phone;
  final String email;
  final String role;
  final String image;

  AuthEntity({
    required this.message,
    required this.token,
    required this.name,
    required this.phone,
    required this.email,
    required this.role,
    required this.image,
  });

 
}
